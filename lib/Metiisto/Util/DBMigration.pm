package Metiisto::Util::DBMigration;
################################################################################
use strict;
################################################################################
use constant UP   => undef;
use constant DOWN => undef;
################################################################################
sub up {
    my ( $class, $dbh ) = @_;
    return ( $class->UP );
}
################################################################################
sub down {
    my ( $class, $dbh ) = @_;
    return ( $class->DOWN );
}
################################################################################
1;
